// ── 시드/해시 유틸 ────────────────────────────────────
// FNV-1a 해시: 같은 문자열은 항상 같은 숫자를 돌려준다(결정적).
function hashStr(str) {
  let h = 2166136261;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0; // 부호 없는 32비트로 정규화
}

// 오늘 날짜를 'YYYY-MM-DD'로 (로컬 기준)
function todayKey() {
  const d = new Date();
  const mm = String(d.getMonth() + 1).padStart(2, "0");
  const dd = String(d.getDate()).padStart(2, "0");
  return `${d.getFullYear()}-${mm}-${dd}`;
}

// 생일+오늘+용도(salt)로 결정적 값 뽑기
function pick(arr, birth, salt) {
  const seed = hashStr(`${birth}|${todayKey()}|${salt}`);
  return arr[seed % arr.length];
}
function pickNum(birth, salt, min, max) {
  const seed = hashStr(`${birth}|${todayKey()}|${salt}`);
  return min + (seed % (max - min + 1));
}

// 1~5점을 채운 별/빈 별 문자열로 (예: 4 → ★★★★☆)
function stars(n) {
  return "★".repeat(n) + "☆".repeat(5 - n);
}

