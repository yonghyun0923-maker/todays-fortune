// URL에 birth 파라미터가 있으면 그대로 결과를 보여준다(공유 대비).
function birthFromUrl() {
  const v = new URLSearchParams(location.search).get("birth");
  // YYYY-MM-DD 형식만 허용 (이상한 값 방어)
  return /^\d{4}-\d{2}-\d{2}$/.test(v || "") ? v : null;
}

